// fetch-achievement-icons.mjs
//
// One-shot asset generator (architecture task T1).
// Downloads every achievement icon referenced in src/data/achievements.ts into
// public/achievement-icons/ and emits src/data/achievement-icon-map.ts so the
// React card layer can resolve a local, hotlink-free icon path.
//
// Constraints (per task spec):
//   - ESM, Node built-ins only: node:fs / node:path / node:crypto + global fetch
//   - Concurrency <= 6
//   - fetch with User-Agent + Referer headers, follow redirects (anti-hotlink)
//   - Filename: <kebab(englishName)>-<sha1(iconUrl).slice(0,8)>.webp
//   - Entries without an icon, or whose download fails, are NOT written to the map
//     (the card layer falls back to a placeholder).
//
// Run: node scripts/fetch-achievement-icons.mjs   (also: npm run fetch:icons)

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs'
import { join, dirname, basename } from 'node:path'
import { fileURLToPath } from 'node:url'
import { createHash } from 'node:crypto'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')
const SRC_FILE = join(ROOT, 'src', 'data', 'achievements.ts')
const OUT_DIR = join(ROOT, 'public', 'achievement-icons')
const MAP_FILE = join(ROOT, 'src', 'data', 'achievement-icon-map.ts')

const CONCURRENCY = 6
const REQUEST_HEADERS = {
  'User-Agent': 'Mozilla/5.0',
  'Referer': 'https://store.steampowered.com/',
}

/**
 * Convert an English achievement name into a filesystem-safe kebab string.
 * Lowercase, replace anything outside [a-z0-9] with '-', trim leading/trailing '-'.
 * @param {string} name
 * @returns {string}
 */
function kebab(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
}

/**
 * First 8 hex chars of the SHA-1 of a string (used to disambiguate icons whose
 * kebab names collide).
 * @param {string} s
 * @returns {string}
 */
function sha1Short(s) {
  return createHash('sha1').update(s).digest('hex').slice(0, 8)
}

/**
 * Extract { englishName, icon } pairs from the achievements source.
 *
 * The tempered greedy token `(?:(?!englishName:)[\s\S])*?` guarantees the matched
 * `icon` belongs to the same object as the `englishName` that opened the match,
 * so an entry missing an `icon` is safely skipped instead of stealing the next
 * entry's icon.
 * @param {string} src
 * @returns {Array<{ englishName: string, icon: string }>}
 */
function parseEntries(src) {
  const re = /englishName:\s*"([^"]+)"(?:(?!englishName:)[\s\S])*?icon:\s*"([^"]+)"/g
  const out = []
  let m
  while ((m = re.exec(src)) !== null) {
    out.push({ englishName: m[1], icon: m[2] })
  }
  return out
}

/**
 * Download a single icon, retrying once on transient failure.
 * Returns the absolute file path on success, or null on failure.
 * @param {{ englishName: string, icon: string }} entry
 * @returns {Promise<string|null>}
 */
async function fetchOne(entry) {
  const { englishName, icon } = entry
  const fname = `${kebab(englishName)}-${sha1Short(icon)}.webp`
  const fpath = join(OUT_DIR, fname)

  // Already downloaded in a previous run → reuse, no network hit.
  if (existsSync(fpath)) {
    return fpath
  }

  for (let attempt = 0; attempt < 2; attempt++) {
    try {
      const res = await fetch(icon, { headers: REQUEST_HEADERS, redirect: 'follow' })
      if (!res.ok) {
        throw new Error(`HTTP ${res.status}`)
      }
      const buf = Buffer.from(await res.arrayBuffer())
      if (buf.length === 0) {
        throw new Error('empty body')
      }
      writeFileSync(fpath, buf)
      return fpath
    } catch (err) {
      if (attempt === 0) {
        // brief backoff before the single retry
        await new Promise((r) => setTimeout(r, 300))
        continue
      }
      console.error(`FAIL [${englishName}] ${icon} -> ${err.message}`)
      return null
    }
  }
  return null
}

/**
 * Run the download pool with bounded concurrency.
 * @param {Array<{ englishName: string, icon: string }>} entries
 * @returns {Promise<Record<string, string>>} englishName -> local public path
 */
async function downloadAll(entries) {
  const map = {}
  let cursor = 0
  let ok = 0
  let fail = 0

  async function worker() {
    while (cursor < entries.length) {
      const i = cursor++
      const fpath = await fetchOne(entries[i])
      if (fpath) {
        ok++
        const fname = basename(fpath)
        map[entries[i].englishName] = `/achievement-icons/${fname}`
      } else {
        fail++
      }
    }
  }

  const pool = Array.from({ length: Math.min(CONCURRENCY, entries.length) }, () => worker())
  await Promise.all(pool)
  return { map, ok, fail }
}

function buildMapSource(map) {
  const header =
    '// AUTO-GENERATED by scripts/fetch-achievement-icons.mjs — do not edit by hand.\n'
  const lines = Object.keys(map).map(
    (k) => `  ${JSON.stringify(k)}: ${JSON.stringify(map[k])},`
  )
  return `${header}export const ACHIEVEMENT_ICON_MAP: Record<string, string> = {\n${lines.join(
    '\n'
  )}\n}\n`
}

async function main() {
  const src = readFileSync(SRC_FILE, 'utf8')
  const entries = parseEntries(src)
  console.log(`Parsed ${entries.length} entries with an icon field.`)

  mkdirSync(OUT_DIR, { recursive: true })

  const { map, ok, fail } = await downloadAll(entries)

  writeFileSync(MAP_FILE, buildMapSource(map))

  console.log(
    `Done. downloaded=${ok} failed=${fail} mapEntries=${Object.keys(map).length}`
  )
  console.log(`Icons dir: ${OUT_DIR}`)
  console.log(`Map file : ${MAP_FILE}`)
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
